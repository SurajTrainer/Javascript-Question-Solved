// demonstrate the use of switch case statement in javascript

let weekdays = 2;
let day ;
switch (weekdays) {
    case 1:
        day = "Sunday";
        break;
      case 2:
        day = "Monday";
        break;
      case 3:
        day = "Tuesday";
        break;
      case 4:
        day = "Wednesday";
        break;
      case 5:
        day = "Thursday";
        break;
      case 6:
        day = "Friday";
        break;

    default:
        break;
}


console.log(day);